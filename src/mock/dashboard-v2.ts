const weekDays = ['周一', '周二', '周三', '周四', '周五', '周六', '周日']
const hourBlocks = ['00:00', '04:00', '08:00', '12:00', '16:00', '20:00']

const buildHeatmap = (base: number, multiplier: number): HeatmapPoint[] => {
  const points: HeatmapPoint[] = []
  weekDays.forEach((weekday, dayIndex) => {
    hourBlocks.forEach((hour, hourIndex) => {
      const wave = ((dayIndex + 1) * (hourIndex + 2)) % 5
      points.push({
        weekday,
        hour,
        value: Math.max(6, Math.round(base + wave * multiplier + dayIndex * 1.2 + hourIndex * 1.8)),
      })
    })
  })
  return points
}

